    private AdvancedSearchDialog(boolean modal) {
        super(GUI.getAppFrame(), modal,
              UserSettings.INSTANCE.getSettings(), null);
        initComponents();
        panel.addNameListener(this);
        setHelpContentsUrl(JptBundle.INSTANCE.getString("Help.Url.Contents"));
    }

    @Override
    public void setVisible(boolean visible) {
        if (visible) {
            panel.readProperties();
            setSearchName(panel.getSearchName());
        }

        super.setVisible(visible);
    }

    private void beforeWindowClosing() {
        panel.writeProperties();
        panel.willDispose();
        setVisible(false);
    }

    public AdvancedSearchPanel getPanel() {
        return panel;
    }

    @Override
    protected void help() {
        help(JptBundle.INSTANCE.getString("Help.Url.AdvancedSearchDialog"));
    }

    @Override
    protected void escape() {
        beforeWindowClosing();
    }

    public AdvancedSearchPanel getAdvancedSearchPanel() {
        return panel;
    }

    @Override
    public void nameChanged(String name) {
        if (name == null) {
            throw new NullPointerException("name == null");
        }

        setSearchName(name);
    }

    private void setSearchName(String name) {
        setTitle(JptBundle.INSTANCE.getString(
                "AdvancedSearchDialog.TitlePrefix") + ": " + name);
    }

